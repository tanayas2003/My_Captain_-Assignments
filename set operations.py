# Define two sets
set_E = {0, 2, 4, 6, 8}
set_N = {1, 2, 3, 4, 5}

# Perform set operations and print the results
print(f"Union of E and N is {set_E | set_N}")
print(f"Intersection of E and N is {set_E & set_N}")
print(f"Difference of E and N is {set_E - set_N}")
print(f"Symmetric difference of E and N is {set_E ^ set_N}")

